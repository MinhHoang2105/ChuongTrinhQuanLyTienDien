package model;

import java.io.Serializable;

/**
 *
 * @author minh
 */
public class ThongKeModel implements Serializable {
    int thang, nam;

    public ThongKeModel() {
    }

    public ThongKeModel(int thang, int nam) {
        this.thang = thang;
        this.nam = nam;
    }

    public int getThang() {
        return thang;
    }

    public void setThang(int thang) {
        this.thang = thang;
    }

    public int getNam() {
        return nam;
    }

    public void setNam(int nam) {
        this.nam = nam;
    }
    
}
